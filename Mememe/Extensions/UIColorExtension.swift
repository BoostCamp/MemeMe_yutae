import Foundation
import UIKit

// App Theme 색상 지정.
extension UIColor {
    // adobe: #3FC1C4
    // Apple 0 184 185
    public class var themeColor : UIColor {
        return UIColor.init(red: 0/255, green: 184/255, blue: 185/255, alpha: 1.0)
    }
}
